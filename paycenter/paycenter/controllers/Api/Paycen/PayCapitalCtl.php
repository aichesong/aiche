class Api_Paycen_PayCapitalCtl extends Api_Controller
{
    /**
     *资金总览
     *
     * @access public
     */
    
    function getCapitallist() {
      
          $User_ResourceModel = new User_ResourceModel();
          $data           = $User_ResourceModel->getResourceList();
            if ($data)
            {
                $msg    = 'success';
                $status = 200;
            }
            else
            {
                $msg    = 'failure';
                $status = 250;
            }
        $this->data->addBody(-140, $data, $msg, $status);
    }
    

}

